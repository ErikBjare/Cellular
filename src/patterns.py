def char_to_cell(char):
    if char == ".":
        return 0
    elif char == "x":
        return 1
    elif char in "0123456789":
        return int(char)
    else:
        raise Exception("Unknown cell value")

def stringlist_to_pattern(stringlist):
    pattern = []
    for row in stringlist:
        pattern.append([])
        for char in row:
            pattern[-1].append(char_to_cell(char))
    return pattern


